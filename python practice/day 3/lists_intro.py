# sum
numbers = [10, 20, 30, 40, 50]
sum = sum(numbers)
print(sum)

# smallest number
numbers = [10, 20, 30, 40, 50]
small = min(numbers)
print(small)

# reversed list
numbers = [10, 20, 30, 40, 50]
reversed_list = numbers[::-1]
print(reversed_list)

# append
numbers = [10, 20, 30, 40, 50]
after = numbers.append(60)
print("after apend,numbers is ", numbers)

# insert
numbers = [10, 20, 30, 40, 50]
after = numbers.insert(2, 25)
print(numbers)

# remove
numbers = [10, 20, 30, 40, 50]
after = numbers.remove(30)
print(numbers)

# pop
numbers = [10, 20, 30, 40, 50]
